<h1>BANKING MANAGEMENT SYSTEM</h1>
<p> A bank is a financial institution which accepts deposits, pays interest on pre-defined rates, clears checks, makes loans, and often acts as an intermediary in financial transactions. It also provides other financial services to its customers.

Bank management governs various concerns associated with bank in order to maximize profits. The concerns broadly include liquidity management, asset management, liability management and capital management </p>
Banking Management System thus ensures smooth operation of the Real-Estate management tasks as well as keep the information about the employees and their salary.<ul>

<p>BANK MANAGEMENT SYSTEM undertaken as a project is based on relevant technologies. The main aim of this project is to develop software for bank management system. This project is to develop software for bank management system. This project has been developed to carry out the processes easily and quickly, which is not possible with the manuals systems, which are overcome by this software. This project is developed using VB language and. Hence it provides the complete solution for the current management system.

